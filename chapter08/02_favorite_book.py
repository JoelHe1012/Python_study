# 喜欢的书
# 编写一个名为favorite_book()的函数，其中包含一个名为title的形参。
# 打印： One of my favorite books is Alice in Wonderland

def favorite_book(title):
    print(f'One of my favorite books is {title}')

favorite_book('Harry Potter')

# 这个函数的工作流程是：
# 将实参'Harry Potter' 赋值给形参 title
# title是在定义函数时使用的变量变量
# 'Harry Potter'是实参，在调用函数时赋值给形参，即在函数中原本是形参的位置用对应的实参来代替
