# 名言1，将一个名人的姓名和名言打印出来
print('Albert Einstein once said, "A person who never made a mistake never tried anything new."') # 单引号和双引号的混合使用
# 名言2，重复上述内容，但是用famous_person 表示名人的名和姓，在创建要显示的消息并将其赋值给变量message,然后再打印这条消息
famous_person = 'Albert Einstein'
message = '"A person who never made a mistake never tried anything new."'
print(f'{famous_person} once said, {message}')